import { Scenes } from 'telegraf'
import { MyContext } from '../../interfaces'
import { saveUserEmail } from '../../core/supabase'

import { mainMenu } from '@/menu/mainMenu'
import { handleBuyRobokassa } from '@/handlers'

export const emailWizard = new Scenes.WizardScene<MyContext>(
  'emailWizard',
  async ctx => {
    if (!ctx.from) {
      throw new Error('User not found')
    }
    const isRu = ctx.from.language_code === 'ru'

    await ctx.reply(
      isRu
        ? 'üëâ –î–ª—è —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è —Å—á–µ—Ç–∞ –Ω–∞–ø–∏—à–∏—Ç–µ –≤–∞—à E-mail.'
        : 'üëâ To generate an invoice, please provide your E-mail.',
      {
        reply_markup: {
          keyboard: [
            [
              {
                text: isRu ? '–û—Ç–º–µ–Ω–∞' : 'Cancel',
              },
            ],
          ],
        },
      }
    )

    return ctx.wizard.next()
  },
  async ctx => {
    const isRu = ctx.from?.language_code === 'ru'
    const msg = ctx.message

    if (msg && 'text' in msg) {
      const email = msg.text

      if (email.toLowerCase() === (isRu ? '–æ—Ç–º–µ–Ω–∞' : 'cancel')) {
        await ctx.reply(
          isRu ? '‚ùå –í—ã–±–æ—Ä e-mail –æ—Ç–º–µ–Ω–µ–Ω.' : '‚ùå Selection e-mail cancelled.',
          {
            reply_markup: {
              keyboard: mainMenu(isRu).reply_markup.keyboard,
            },
          }
        )
        return ctx.scene.leave()
      }

      if (email.includes('@')) {
        try {
          if (!ctx.from) {
            throw new Error('User not found')
          }
          await saveUserEmail(ctx.from.id.toString(), email)
          await ctx.reply(
            isRu
              ? '–í–∞—à e-mail —É—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω. –¢–µ–ø–µ—Ä—å –≤—ã –º–æ–∂–µ—Ç–µ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç—å –æ–ø–ª–∞—Ç—É.'
              : 'Your e-mail has been successfully saved. You can now proceed with the payment.'
          )
          await handleBuyRobokassa({ ctx, isRu })
        } catch (error) {
          await ctx.reply(
            isRu
              ? '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ e-mail. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.'
              : 'Error saving e-mail. Please try again.'
          )
        }
        return ctx.scene.leave()
      } else {
        await ctx.reply(
          isRu
            ? '–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π e-mail. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.'
            : 'Invalid e-mail. Please try again.'
        )
      }
    }
    return ctx.scene.leave()
  }
)

export default emailWizard
